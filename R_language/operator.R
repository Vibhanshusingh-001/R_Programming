c1<-c(2,4,55)
c2<-c(3,5,96)
#here c is vector this is for collection of similar type of datatypes

print(c1+c2)
print(c1-c2)
print(c1*c2)
print(c1/c2)
print(c1%%c2)
print(c1%/%c2)
print(c1^c2)


#relational operator { <  >  <=  >==  == != }

c1<-c(2,4,55)
c2<-c(3,5,96)

print(c1<c2)
print(c1>c2)
print(c1<=c2)
print(c1>=c2)
print(c1!=c2)
print(c1==c2)


#logical operator{ & | ! && || }


c1<-c(2,4,55)
c2<-c(3,5,96)

print(c1&c2)
print(c1&&c2)
print(c1!c2)
print(c1|c2)
print(c1||c2)

